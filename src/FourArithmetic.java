public class FourArithmetic {
    public static void main(String[] args) {
        int a, b;
        a = 12;
        b = 2;

        System.out.println("a + b = " + (a + b)); // 문자열 + 정수 일때, +의 의미는 '결합'
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
    }
}
